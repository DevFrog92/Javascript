// 표현식과 문

// 값
// 값은 표현식(expression)이 평가(evaluate)되어 생성된 결과물이다. 
// 여기서 평가란 식을 해석해서 값을 생성하거나 참조하는 것을 의미한다.
// 모든 값은 데이터 타입을 가진다.
// 여기서 값은 식의 평가 이후에 할당된다. 

// 리터럴
// 리터럴은 사람이 이해할 수 있는 문자 또는 약속된 기호를 사용해 값을 생성하는 표기법을 말한다. 
// ex) 3 숫자 리터럴
// 자바스크립트는 런타임에 리터럴을 평가해 값을 생성한다.

// 표현식
// 표현식은 값으로 평가될 수 있는 문(statement)이다. 즉, 표현식이 평가되면 새로운 값을 생성하거나 기존 값을 참조한다.
// 값으로 평가될 수 있는 문은 모두 표현식이다.